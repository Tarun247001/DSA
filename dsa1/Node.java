package dsa1;


public class Node {
	private int data;
	private Node next;
	
	public Node(int data) {
		this.data=data;
		this.next=next;
	}
	public int getdata() {
		return data;
	}
	public void setdata(int data) {
		this.data=data;
	}
	public Node getnext() {
		return next;
	}
	public void setnext(Node next) {
		this.next=next;
	}
}
